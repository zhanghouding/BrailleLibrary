	//得到编码对应的英文字符串
	public static String getEnString( char code )
	{
		for( int i = 0; i < CODE.length; i++ )
		{
			if( CODE[i] == code )
			{
				return	EN[i];
			}
		}
		
		return	null;
	}
	
	//得到编码对应的中文字符串
	public static String getCnString( char code )
	{
		for( int i = 0; i < CODE.length; i++ )
		{
			if( CODE[i] == code )
			{
				return	CN[i];
			}
		}
		
		return	null;
	}	
